function normalizeText(text) {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^\w\s]/gi, "")
    .trim();
}

// Detecta idioma simples por palavras-chave
function detectLanguage(text) {
  const enWords = [
    "what", "where", "your", "name", "favorite", "food", "how", "old",
    "do", "you", "like", "language", "games", "programming", "hobby"
  ];

  const ptWords = [
    "qual", "onde", "seu", "sua", "nome", "comida", "favorita", "idade",
    "voce", "gosta", "linguagem", "jogos", "logica", "programacao", "hobby"
  ];

  let enCount = 0;
  let ptCount = 0;
  const words = text.toLowerCase().split(/\s+/);

  words.forEach(word => {
    if (enWords.includes(word)) enCount++;
    if (ptWords.includes(word)) ptCount++;
  });

  return enCount >= ptCount ? "en" : "pt";
}

// Pega uma resposta aleatória de um array
function getRandomResponse(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

// Processa a mensagem do usuário
function processMessage() {
  const inputField = document.getElementById("user_input");
  const rawInput = inputField.value;
  const userInput = normalizeText(rawInput);
  const chat = document.getElementById("chat");

  if (!userInput) return;

  const lang = detectLanguage(rawInput);

  let response = lang === "pt"
    ? "Desculpe, não sei a resposta para isso."
    : "Sorry, I don't know the answer.";

  for (let key in responses) {
    if (userInput.includes(key)) {
      response = getRandomResponse(responses[key][lang]);
      break;
    }
  }

  // Exibe mensagens como <li> com classes e tags fortes
  const userMsg = document.createElement("li");
  userMsg.className = "user-message";
  userMsg.innerHTML = `<strong>Você:</strong> ${rawInput}`;
  chat.appendChild(userMsg);

  const botMsg = document.createElement("li");
  botMsg.className = "bot-message";
  botMsg.innerHTML = `<strong>FlashBot:</strong> ${response}`;
  chat.appendChild(botMsg);

  chat.scrollTop = chat.scrollHeight;
  inputField.value = "";
  inputField.focus();
}

// Enviar com Enter
document.getElementById("user_input").addEventListener("keypress", function (e) {
  if (e.key === "Enter") {
    processMessage();
  }
});

// Fundo com efeito de estrelas surgindo e sumindo
function createStar() {
  const star = document.createElement("div");
  star.className = "star";
  star.style.left = `${Math.random() * 100}vw`;
  star.style.top = `${Math.random() * 100}vh`;
  star.style.animationDuration = `${2 + Math.random() * 3}s`; // entre 2s e 5s

  document.body.appendChild(star);

  setTimeout(() => {
    star.remove();
  }, 5000);
}

